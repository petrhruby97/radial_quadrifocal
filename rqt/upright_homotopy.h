template <unsigned N, typename F> struct minus_array { // Speed critical -----------------------------------------
    static inline void multiply_scalar_to_self(std::complex<F> *__restrict__ a, std::complex<F> b) {
        for (unsigned i = 0; i < N; ++i, ++a)
            *a = *a * b;
    }

    static inline void negate_self(std::complex<F> *__restrict__ a) {
        for (unsigned i = 0; i < N; ++i, ++a)
            *a = -*a;
    }

    static inline void multiply_self(std::complex<F> *__restrict__ a, const std::complex<F> *__restrict__ b) {
        for (unsigned int i = 0; i < N; ++i, ++a, ++b)
            *a *= *b;
    }

    static inline void add_to_self(std::complex<F> *__restrict__ a, const std::complex<F> *__restrict__ b) {
        for (unsigned int i = 0; i < N; ++i, ++a, ++b)
            *a += *b;
    }

    static inline void add_scalar_to_self(std::complex<F> *__restrict__ a, std::complex<F> b) {
        for (unsigned int i = 0; i < N; ++i, ++a)
            *a += b;
    }

    static inline void copy(const std::complex<F> *__restrict__ a, std::complex<F> *__restrict__ b) {
        memcpy(b, a, N * sizeof(std::complex<F>));
    }

    static inline F norm2(const std::complex<F> *__restrict__ a) {
        F val = 0;
        std::complex<F> const *__restrict__ end = a + N;
        while (a != end)
            val += std::norm(*a++);
        return val;
    }
};

